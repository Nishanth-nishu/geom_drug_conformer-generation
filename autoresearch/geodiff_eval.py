"""
geodiff_eval.py — GeoDiff-style COV-MAT Evaluation for mol_next_gen

Ports the evaluation strategy from GeoDiff (Xu et al., ICML 2022) into our
mol_next_gen framework. This gives the STANDARD benchmark metrics used in all
published conformer generation papers:

  COV-R  — Coverage Recall: % of reference conformers covered by ≥1 generated conformer
  MAT-R  — Matching Recall: mean best-RMSD of each reference conformer to generated set
  COV-P  — Coverage Precision: % of generated conformers covered by ≥1 reference
  MAT-P  — Matching Precision: mean best-RMSD of generated conformers to reference

Reference: Xu et al. "GeoDiff: A Geometric Diffusion Model for Molecular Conformation 
Generation", ICML 2022. Table 1.

Benchmark SOTA on QM9 heavy-atom:
  GeoMol (NeurIPS 2021): COV-R=71.5%, MAT-R=0.225 Å
  GeoDiff (ICML 2022):   COV-R=71.0%, MAT-R=0.297 Å

Usage:
  python autoresearch/geodiff_eval.py --ckpt checkpoints/exp_A_baseline_best.pt
  python autoresearch/geodiff_eval.py --ckpt checkpoints/exp_G_best.pt --heavy-atom
"""

