# ─────────────────────────────────────────────────────────────────────────────
# Kabsch RMSD (no RDKit required for initial testing)
# ─────────────────────────────────────────────────────────────────────────────

def kabsch_align(P: np.ndarray, Q: np.ndarray) -> float:
    """
    Kabsch algorithm: minimum RMSD between two point sets after optimal rotation.
    P, Q: (N, 3) numpy arrays (already centered).
    Returns scalar RMSD.
    """
    P = P - P.mean(0)
    Q = Q - Q.mean(0)
    H = P.T @ Q
    U, S, Vt = np.linalg.svd(H)
    D = np.eye(3)
    D[2, 2] = np.sign(np.linalg.det(Vt.T @ U.T))
    R = Vt.T @ D @ U.T
    P_rot = P @ R.T
    return float(np.sqrt(np.mean((P_rot - Q) ** 2)))


def covmat_single_molecule(ref_conformers: list, gen_conformers: list,
                            thresholds: np.ndarray) -> tuple:
    """
    Compute COV-MAT metrics for a single molecule.
    
    Args:
        ref_conformers: list of (N, 3) numpy arrays (reference conformers from dataset)
        gen_conformers: list of (N, 3) numpy arrays (generated by model)
        thresholds: array of RMSD thresholds for COV metric

    Returns:
        (cov_r, mat_r, cov_p, mat_p, rmsd_matrix)
        cov_r, cov_p: arrays of shape (num_thresholds,)
        mat_r, mat_p: scalars
    """
    num_ref = len(ref_conformers)
    num_gen = len(gen_conformers)

    # Build full RMSD confusion matrix: (num_ref, num_gen)
    rmsd_mat = np.zeros((num_ref, num_gen))
    for i, ref in enumerate(ref_conformers):
        for j, gen in enumerate(gen_conformers):
            rmsd_mat[i, j] = kabsch_align(ref, gen)

    # MAT-R: for each reference, minimum RMSD to any generated conformer
    rmsd_ref_min = rmsd_mat.min(axis=1)  # (num_ref,)
    mat_r = float(rmsd_ref_min.mean())
    cov_r = np.array([(rmsd_ref_min <= thr).mean() for thr in thresholds])

    # MAT-P: for each generated, minimum RMSD to any reference conformer
    rmsd_gen_min = rmsd_mat.min(axis=0)  # (num_gen,)
    mat_p = float(rmsd_gen_min.mean())
    cov_p = np.array([(rmsd_gen_min <= thr).mean() for thr in thresholds])

    return cov_r, mat_r, cov_p, mat_p


