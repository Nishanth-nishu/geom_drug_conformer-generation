def main():
    parser = argparse.ArgumentParser(description="GeoDiff COV-MAT Evaluation")
    parser.add_argument('--ckpt', type=str, required=True,
                        help='Path to model checkpoint (.pt)')
    parser.add_argument('--data', type=str, default=None,
                        help='Dataset JSONL path (default: qm9_heavy.jsonl if exists, else qm9_selfies.jsonl)')
    parser.add_argument('--heavy-atom', action='store_true',
                        help='Use qm9_heavy.jsonl (max 9 atoms)')
    parser.add_argument('--max-atoms', type=int, default=None,
                        help='Max atoms per molecule (default: 9 for heavy, 29 for all)')
    parser.add_argument('--n-mols', type=int, default=500,
                        help='Number of molecules to evaluate')
    parser.add_argument('--n-gen', type=int, default=10,
                        help='Number of conformers to generate per molecule')
    parser.add_argument('--device', type=str, default='auto',
                        help='Device: cuda, cpu, or auto')
    parser.add_argument('--out-tsv', type=str, default=None,
                        help='Output TSV path (default: autoresearch/results_geodiff.tsv)')
    parser.add_argument('--description', type=str, default='',
                        help='Description for TSV row')
    args = parser.parse_args()

    # Device
    if args.device == 'auto':
        device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    else:
        device = torch.device(args.device)
    print(f"Device: {device}")

    # Data path
    _here = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    if args.data:
        data_path = args.data
    elif args.heavy_atom or os.path.exists(os.path.join(_here, 'data', 'qm9_heavy.jsonl')):
        data_path = os.path.join(_here, 'data', 'qm9_heavy.jsonl')
        print(f"Using heavy-atom dataset: {data_path}")
    else:
        data_path = os.path.join(_here, 'data', 'qm9_selfies.jsonl')
        print(f"Using full dataset: {data_path}")

    max_atoms = args.max_atoms
    if max_atoms is None:
        max_atoms = 9 if ('heavy' in data_path) else 29

    print(f"Loading dataset from: {data_path} (max_atoms={max_atoms})")
    dataset = load_dataset(data_path, max_atoms=max_atoms, max_mols=args.n_mols)
    print(f"Loaded {len(dataset)} molecules for evaluation")

    # Load model
    print(f"\nLoading checkpoint: {args.ckpt}")
    ckpt = torch.load(args.ckpt, map_location=device, weights_only=False)
    config = ckpt.get('config', {})
    print(f"  Config: {config}")

    from models.conformer_diffusion import ConformerDiffusion
    model = ConformerDiffusion(
        num_timesteps=config.get('timesteps', 1000),
        hidden_dim=config.get('hidden_dim', 256),
        num_layers=config.get('num_layers', 6),
        time_dim=config.get('time_dim', 128),
        num_rbf=config.get('num_rbf', 20),
    ).to(device)
    model.load_state_dict(ckpt['model_state_dict'])
    model.eval()
    n_params = sum(p.numel() for p in model.parameters()) / 1e6
    print(f"  Parameters: {n_params:.2f}M")

    # Run eval
    print(f"\nRunning COV-MAT eval: {len(dataset)} mols × {args.n_gen} conformers...")
    results = run_geodiff_eval(
        model, dataset, device,
        num_gen_per_mol=args.n_gen,
        verbose=True
    )

    # Print results
    tag = f"[{os.path.basename(args.ckpt)}]"
    print_geodiff_results(results, tag=tag)

    # Write TSV
    out_tsv = args.out_tsv or os.path.join(_here, 'autoresearch', 'results_geodiff.tsv')
    description = args.description or f"n_gen={args.n_gen}, max_atoms={max_atoms}"
    write_results_tsv(results, args.ckpt, out_tsv, description=description)


if __name__ == '__main__':
    main()
